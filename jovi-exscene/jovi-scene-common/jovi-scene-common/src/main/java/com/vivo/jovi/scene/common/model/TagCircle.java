package com.vivo.jovi.scene.common.model;

public class TagCircle {

    private String groupName;

    private String booleanStr;

    private long numHits;

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }

    public String getBooleanStr() {
        return booleanStr;
    }

    public void setBooleanStr(String booleanStr) {
        this.booleanStr = booleanStr;
    }

    public long getNumHits() {
        return numHits;
    }

    public void setNumHits(long numHits) {
        this.numHits = numHits;
    }
}
